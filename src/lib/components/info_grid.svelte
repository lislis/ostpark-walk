    <h2>
        <span  class="a11y-hidden">Audio</span>
        <img src="/icons/audio.svg" width="40" height="40" alt=""></h2>
    <div>
        <p>
        Das Lautsprecher-Symbol steht für Medien, die man hören kann.
       Wenn du in einem Umkreis von 10 Meter des markierten Ortes bist, fängt die Markierung an zu hüpfen, ein Player erscheint und du kannst das Audio abspielen.
       Wenn du auf "Transkript anzeigen" klickst, bekommst du das ganze als Text angezeigt. 

        </p>
    </div>

